    private void get_result2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_get_result2ActionPerformed
        // calculer  Propagation_model_path_loss :
     //get value of distance
     double d = Double.parseDouble(distance.getText());   
      //get value of frequency
     double f = Double.parseDouble(frequence.getText()); 
     // Calculer le FSPL en utilisant la formule donnée
        double fspl = 20 * Math.log10(d) + 20 * Math.log10(f) - 27.55;
        // Afficher le résultat
        Propagation_model_path_loss1.setText(String.format("%.2f dB ", fspl));
        
        
        
         // calculer  Full_path_loss :
     //get value of BodyLoss
     double BodyLoss = Double.parseDouble(jTextFieldBodyLoss.getText());   
      //get value of FoliageLoss
     double FoliageLoss = Double.parseDouble(jTextFieldFoliageLoss.getText()); 
     //get value of SlowFadingMargin
     double SlowFadingMargin = Double.parseDouble(jTextFieldSlowFadingMargin.getText());   
      //get value of Rain
     double Rain = Double.parseDouble(jTextFieldRain.getText()); 
       //get value of InterferenceMargin
     double InterferenceMargin = Double.parseDouble(jTextFieldInterferenceMargin.getText()); 
     // Calculer le Full_path_loss en utilisant la formule donnée
        double full_path_loss =InterferenceMargin+Rain+SlowFadingMargin+FoliageLoss+BodyLoss+fspl ;
        // Afficher le résultat
        jTextFieldFullPathLoss1.setText(String.format("%.2f dB ", full_path_loss));
        
         // calculer  link budget :
     //get value of Transmit power from user terminal
     double transmitPower = Double.parseDouble(transmitPowerUt.getText());   
      //get value of antennaGain from user terminal
     double antennaGain1 = Double.parseDouble(antennaGainUt.getText()); 
     //get value of cableLoss from user terminal
     double cableLoss1 = Double.parseDouble(cableLossUt.getText());   
     
     //get value of Transmit power from gNodeB
     int antennaGain2 = (int) antennaGainGN.getValue();
     int cableLoss2 = (int) cableLossGN.getValue();
     
     
      
     
     
     // Calculer link budget en utilisant la formule donnée
        double link_bugdet =transmitPower+antennaGain1-cableLoss1+antennaGain2-cableLoss2-full_path_loss ;
        // Afficher le résultat
        LinkBudget1.setText(String.format("%.2f dB ", link_bugdet));
        
        

  // Model status
       int Input_PRB = (int) input_PRB.getValue();
      double link_budget_abs = Math.abs(link_bugdet);
              
if (Input_PRB == 0 && link_budget_abs <= 119.44) {
   jTextFieldStatus1.setText("fail");
   
} else if (Input_PRB == 1 && link_budget_abs >= 119.45) {
    jTextFieldStatus1.setText("fail");
    
} else if (Input_PRB == 1 && link_budget_abs <= 119.44) {
      jTextFieldStatus1.setText("pass");
     
}


    }//GEN-LAST:event_get_result2ActionPerformed


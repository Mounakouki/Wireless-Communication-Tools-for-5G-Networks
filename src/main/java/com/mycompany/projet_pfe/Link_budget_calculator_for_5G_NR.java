    private void entrer3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrer3ActionPerformed
       // Receiver sensitivity (dBm) = Noise figure (dB) + Thermal noise (dBm) + SINR (dB)
        //Thermal noise =-174+10log("bandwidth" Hz)
        //PRB Bandwidth =12*lamdavaleur
      //  int subcarrierQuantity;
      double lambdaValue = 0.0;
       

            if (subcarrierspacing0.isSelected()) {
                lambdaValue = 15.0;
            } else if (subcarrierspacing1.isSelected()) {
               lambdaValue = 30.0;
            } else if (subcarrierspacing2.isSelected()) {
               lambdaValue = 60.0;
           } else if (subcarrierspacing3.isSelected()) {
                lambdaValue = 120.0;
           } else if (subcarrierspacing4.isSelected()) {
                lambdaValue = 240.0;
           }

            double prbBandwidth = 12.0 * lambdaValue;
            jTextField2.setText(String.format("%.2f kHz", prbBandwidth));
            double thermalNoise = -174 + 10 * Math.log10(prbBandwidth * 1000);
            // Afficher le bruit thermique dans le JTextField
         jTextFieldThermalnoise.setText(String.format("%.2f", thermalNoise));
         
            int noiseFigure = (int) jSpinnerNoise.getValue(); // Récupérer la valeur de la figure de bruit
             int sinr = (int) jSpinnerSINR.getValue(); // Récupérer la valeur de SINR
             double receiverSensitivity = noiseFigure + thermalNoise + sinr;

             // Afficher la sensibilité du récepteur dans le JTextField
             jTextFieldResult.setText(String.format("%.2f dB ", receiverSensitivity));
             jTextFieldResult.repaint(); 


  
 

    }//GEN-LAST:event_entrer3ActionPerformed


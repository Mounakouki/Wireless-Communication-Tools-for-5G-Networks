private void acceptNumbersOnly(JTextField textField) {
  
    textField.addKeyListener(new KeyAdapter() {
        public void keyTyped(KeyEvent e) {
            char c = e.getKeyChar();
            //if (!(Character.isDigit(c) || c == '.')) {
               // e.consume();
             if (!(Character.isDigit(c) || c == '.' || c == ',')) {
                e.consume();
            }
            // Replace ',' with '.' for decimal input
            if (c == ',') {
                e.setKeyChar('.');
            }
            
        }
    });
}

    
    
